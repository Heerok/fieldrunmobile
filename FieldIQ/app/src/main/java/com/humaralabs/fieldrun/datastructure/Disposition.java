package com.humaralabs.fieldrun.datastructure;



public class Disposition {

    public String name;
    public String value;
    public String type;
    public String actiontype;

    public Disposition(String name,String value,String type,String actiontype)  {
        this.name = name;
        this.value = value;
        this.type = type;
        this.actiontype = actiontype;
    }
}
